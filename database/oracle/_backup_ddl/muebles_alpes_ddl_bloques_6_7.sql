-- ============================================================================
-- BLOQUE 6: INVENTARIOS Y BODEGAS
-- ============================================================================
-- Descripción: Gestión de inventario, movimientos, bodegas y stock
-- Incluye: Control de existencias, ajustes, tomas físicas, reservas
-- ============================================================================

-- ----------------------------------------------------------------------------
-- Tabla: ALP_BODEGA
-- Descripción: Bodegas o almacenes físicos
-- Ejemplos: 'Bodega Central', 'Almacén Zona 10', 'Bodega Producción'
-- ----------------------------------------------------------------------------
CREATE TABLE ALP_BODEGA (
    BOD_BODEGA               NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    BOD_CODIGO               VARCHAR2(50) NOT NULL UNIQUE,
    BOD_NOMBRE               VARCHAR2(150) NOT NULL,
    BOD_DIRECCION            VARCHAR2(500),
    BOD_ESTADO               VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,
    BOD_FECHA_CREACION       TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    
    CONSTRAINT CHK_BOD_ESTADO CHECK (BOD_ESTADO IN ('ACTIVO', 'INACTIVO', 'MANTENIMIENTO'))
);

COMMENT ON TABLE ALP_BODEGA IS 'Catálogo de bodegas y almacenes físicos';

CREATE INDEX IDX_BOD_ESTADO ON ALP_BODEGA(BOD_ESTADO);

-- ----------------------------------------------------------------------------
-- Tabla: ALP_ZONA_BODEGA
-- Descripción: Zonas o ubicaciones dentro de una bodega
-- Ejemplos: 'Zona A-1', 'Pasillo 5', 'Rack 10'
-- ----------------------------------------------------------------------------
CREATE TABLE ALP_ZONA_BODEGA (
    ZBO_ZONA_BODEGA          NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    BOD_BODEGA               NUMBER NOT NULL,
    ZBO_CODIGO               VARCHAR2(50) NOT NULL,
    ZBO_NOMBRE               VARCHAR2(150) NOT NULL,
    ZBO_DESCRIPCION          VARCHAR2(500),
    ZBO_ESTADO               VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,
    ZBO_FECHA_CREACION       TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    
    CONSTRAINT FK_ZBO_BODEGA FOREIGN KEY (BOD_BODEGA) 
        REFERENCES ALP_BODEGA(BOD_BODEGA),
    CONSTRAINT UNQ_ZBO_BODEGA_CODIGO UNIQUE (BOD_BODEGA, ZBO_CODIGO),
    CONSTRAINT CHK_ZBO_ESTADO CHECK (ZBO_ESTADO IN ('ACTIVO', 'INACTIVO', 'BLOQUEADO'))
);

COMMENT ON TABLE ALP_ZONA_BODEGA IS 'Ubicaciones y zonas dentro de bodegas';

CREATE INDEX IDX_ZBO_BODEGA ON ALP_ZONA_BODEGA(BOD_BODEGA);
CREATE INDEX IDX_ZBO_CODIGO ON ALP_ZONA_BODEGA(ZBO_CODIGO);

-- ----------------------------------------------------------------------------
-- Tabla: ALP_TIPO_MOVIMIENTO
-- Descripción: Tipos de movimientos de inventario
-- Ejemplos: 'ENTRADA_COMPRA', 'SALIDA_VENTA', 'AJUSTE_POSITIVO', 'TRANSFERENCIA'
-- ----------------------------------------------------------------------------
CREATE TABLE ALP_TIPO_MOVIMIENTO (
    TMO_TIPO_MOVIMIENTO      NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    TMO_CODIGO               VARCHAR2(50) NOT NULL UNIQUE,
    TMO_NOMBRE               VARCHAR2(150) NOT NULL,
    TMO_AFECTA_STOCK         VARCHAR2(1) NOT NULL,
    TMO_DESCRIPCION          VARCHAR2(500),
    
    CONSTRAINT CHK_TMO_AFECTA CHECK (TMO_AFECTA_STOCK IN ('S', 'N'))
);

COMMENT ON TABLE ALP_TIPO_MOVIMIENTO IS 'Catálogo de tipos de movimientos de inventario';
COMMENT ON COLUMN ALP_TIPO_MOVIMIENTO.TMO_AFECTA_STOCK IS 'S = modifica existencias, N = solo registro';

-- ----------------------------------------------------------------------------
-- Tabla: ALP_INVENTARIO_TIPO_MOVIMIENTO (Redundante con ALP_TIPO_MOVIMIENTO)
-- Nota: Parece duplicada, se mantiene por si hay lógica específica diferente
-- ----------------------------------------------------------------------------
CREATE TABLE ALP_INVENTARIO_TIPO_MOVIMIENTO (
    ITM_INVENTARIO_TIPO_MOV  NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    ITM_CODIGO               VARCHAR2(50) NOT NULL UNIQUE,
    ITM_NOMBRE               VARCHAR2(150) NOT NULL,
    ITM_DESCRIPCION          VARCHAR2(500),
    ITM_ESTADO               VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,
    
    CONSTRAINT CHK_ITM_ESTADO CHECK (ITM_ESTADO IN ('ACTIVO', 'INACTIVO'))
);

COMMENT ON TABLE ALP_INVENTARIO_TIPO_MOVIMIENTO IS 'Catálogo alternativo de tipos de movimiento';

-- ----------------------------------------------------------------------------
-- Tabla: ALP_EXISTENCIA
-- Descripción: Stock actual por producto/variante en cada bodega/zona
-- CRÍTICO: Tabla de control de inventario en tiempo real
-- ----------------------------------------------------------------------------
CREATE TABLE ALP_EXISTENCIA (
    EXI_EXISTENCIA           NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    PRO_PRODUCTO             NUMBER,
    PVA_PRODUCTO_VARIANTE    NUMBER,
    BOD_BODEGA               NUMBER NOT NULL,
    ZBO_ZONA_BODEGA          NUMBER,
    EXI_CANTIDAD_DISPONIBLE  NUMBER(12,2) DEFAULT 0 NOT NULL,
    EXI_CANTIDAD_RESERVADA   NUMBER(12,2) DEFAULT 0 NOT NULL,
    EXI_CANTIDAD_MINIMA      NUMBER(12,2),
    EXI_CANTIDAD_MAXIMA      NUMBER(12,2),
    EXI_PUNTO_REORDEN        NUMBER(12,2),
    EXI_ULTIMA_ACTUALIZACION TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    
    CONSTRAINT FK_EXI_PRODUCTO FOREIGN KEY (PRO_PRODUCTO) 
        REFERENCES ALP_PRODUCTO(PRO_PRODUCTO),
    CONSTRAINT FK_EXI_VARIANTE FOREIGN KEY (PVA_PRODUCTO_VARIANTE) 
        REFERENCES ALP_PRODUCTO_VARIANTE(PVA_PRODUCTO_VARIANTE),
    CONSTRAINT FK_EXI_BODEGA FOREIGN KEY (BOD_BODEGA) 
        REFERENCES ALP_BODEGA(BOD_BODEGA),
    CONSTRAINT FK_EXI_ZONA FOREIGN KEY (ZBO_ZONA_BODEGA) 
        REFERENCES ALP_ZONA_BODEGA(ZBO_ZONA_BODEGA),
    CONSTRAINT CHK_EXI_PRODUCTO_O_VARIANTE CHECK (
        (PRO_PRODUCTO IS NOT NULL AND PVA_PRODUCTO_VARIANTE IS NULL) OR
        (PRO_PRODUCTO IS NULL AND PVA_PRODUCTO_VARIANTE IS NOT NULL)
    ),
    CONSTRAINT CHK_EXI_CANTIDADES CHECK (
        EXI_CANTIDAD_DISPONIBLE >= 0 AND 
        EXI_CANTIDAD_RESERVADA >= 0
    )
);

COMMENT ON TABLE ALP_EXISTENCIA IS 'Stock actual por ubicación (inventario perpetuo)';
COMMENT ON COLUMN ALP_EXISTENCIA.EXI_CANTIDAD_DISPONIBLE IS 'Stock libre para venta';
COMMENT ON COLUMN ALP_EXISTENCIA.EXI_CANTIDAD_RESERVADA IS 'Stock comprometido en órdenes';
COMMENT ON COLUMN ALP_EXISTENCIA.EXI_PUNTO_REORDEN IS 'Nivel de reorden automático';

CREATE INDEX IDX_EXI_PRODUCTO ON ALP_EXISTENCIA(PRO_PRODUCTO);
CREATE INDEX IDX_EXI_VARIANTE ON ALP_EXISTENCIA(PVA_PRODUCTO_VARIANTE);
CREATE INDEX IDX_EXI_BODEGA ON ALP_EXISTENCIA(BOD_BODEGA);
CREATE INDEX IDX_EXI_ZONA ON ALP_EXISTENCIA(ZBO_ZONA_BODEGA);
CREATE INDEX IDX_EXI_DISPONIBLE ON ALP_EXISTENCIA(EXI_CANTIDAD_DISPONIBLE);

-- ----------------------------------------------------------------------------
-- Tabla: ALP_MOVIMIENTO_INVENTARIO
-- Descripción: Registro de movimientos de inventario (Kardex)
-- CRÍTICO: Auditoría completa de entradas/salidas
-- ----------------------------------------------------------------------------
CREATE TABLE ALP_MOVIMIENTO_INVENTARIO (
    MIN_MOVIMIENTO_INVENTARIO NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    PRO_PRODUCTO             NUMBER,
    PVA_PRODUCTO_VARIANTE    NUMBER,
    BOD_BODEGA               NUMBER NOT NULL,
    ZBO_ZONA_BODEGA          NUMBER,
    TMO_TIPO_MOVIMIENTO      NUMBER NOT NULL,
    VEN_ORDEN_VENTA          NUMBER,
    MIN_CANTIDAD             NUMBER(12,2) NOT NULL,
    MIN_COSTO_UNITARIO       NUMBER(12,2),
    MIN_FECHA_MOVIMIENTO     TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    MIN_OBSERVACION          VARCHAR2(1000),
    MIN_REFERENCIA_EXTERNA   VARCHAR2(100),
    MIN_FECHA_CREACION       TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    
    CONSTRAINT FK_MIN_PRODUCTO FOREIGN KEY (PRO_PRODUCTO) 
        REFERENCES ALP_PRODUCTO(PRO_PRODUCTO),
    CONSTRAINT FK_MIN_VARIANTE FOREIGN KEY (PVA_PRODUCTO_VARIANTE) 
        REFERENCES ALP_PRODUCTO_VARIANTE(PVA_PRODUCTO_VARIANTE),
    CONSTRAINT FK_MIN_BODEGA FOREIGN KEY (BOD_BODEGA) 
        REFERENCES ALP_BODEGA(BOD_BODEGA),
    CONSTRAINT FK_MIN_ZONA FOREIGN KEY (ZBO_ZONA_BODEGA) 
        REFERENCES ALP_ZONA_BODEGA(ZBO_ZONA_BODEGA),
    CONSTRAINT FK_MIN_TIPO_MOVIMIENTO FOREIGN KEY (TMO_TIPO_MOVIMIENTO) 
        REFERENCES ALP_TIPO_MOVIMIENTO(TMO_TIPO_MOVIMIENTO),
    CONSTRAINT CHK_MIN_PRODUCTO_O_VARIANTE CHECK (
        (PRO_PRODUCTO IS NOT NULL AND PVA_PRODUCTO_VARIANTE IS NULL) OR
        (PRO_PRODUCTO IS NULL AND PVA_PRODUCTO_VARIANTE IS NOT NULL)
    ),
    CONSTRAINT CHK_MIN_CANTIDAD CHECK (MIN_CANTIDAD != 0)
);

COMMENT ON TABLE ALP_MOVIMIENTO_INVENTARIO IS 'Registro histórico de movimientos (Kardex)';
COMMENT ON COLUMN ALP_MOVIMIENTO_INVENTARIO.MIN_CANTIDAD IS 'Cantidad (+entrada, -salida)';
COMMENT ON COLUMN ALP_MOVIMIENTO_INVENTARIO.VEN_ORDEN_VENTA IS 'FK a orden de venta (si aplica)';

CREATE INDEX IDX_MIN_PRODUCTO ON ALP_MOVIMIENTO_INVENTARIO(PRO_PRODUCTO);
CREATE INDEX IDX_MIN_VARIANTE ON ALP_MOVIMIENTO_INVENTARIO(PVA_PRODUCTO_VARIANTE);
CREATE INDEX IDX_MIN_BODEGA ON ALP_MOVIMIENTO_INVENTARIO(BOD_BODEGA);
CREATE INDEX IDX_MIN_TIPO ON ALP_MOVIMIENTO_INVENTARIO(TMO_TIPO_MOVIMIENTO);
CREATE INDEX IDX_MIN_FECHA ON ALP_MOVIMIENTO_INVENTARIO(MIN_FECHA_MOVIMIENTO);

-- ----------------------------------------------------------------------------
-- Tabla: ALP_AJUSTE_STOCK
-- Descripción: Ajustes manuales de inventario
