    USU_USUARIO              NUMBER NOT NULL,
    AJS_FECHA_AJUSTE         TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    
    CONSTRAINT FK_AJS_PRODUCTO FOREIGN KEY (PRO_PRODUCTO) 
        REFERENCES ALP_PRODUCTO(PRO_PRODUCTO),
    CONSTRAINT FK_AJS_VARIANTE FOREIGN KEY (PVA_PRODUCTO_VARIANTE) 
        REFERENCES ALP_PRODUCTO_VARIANTE(PVA_PRODUCTO_VARIANTE),
    CONSTRAINT FK_AJS_BODEGA FOREIGN KEY (BOD_BODEGA) 
        REFERENCES ALP_BODEGA(BOD_BODEGA),
    CONSTRAINT FK_AJS_USUARIO FOREIGN KEY (USU_USUARIO) 
        REFERENCES ALP_USUARIO(USU_USUARIO),
    CONSTRAINT CHK_AJS_PRODUCTO_O_VARIANTE CHECK (
        (PRO_PRODUCTO IS NOT NULL AND PVA_PRODUCTO_VARIANTE IS NULL) OR
        (PRO_PRODUCTO IS NULL AND PVA_PRODUCTO_VARIANTE IS NOT NULL)
    )
);

COMMENT ON TABLE ALP_AJUSTE_STOCK IS 'Ajustes manuales de inventario (auditoría obligatoria)';

CREATE INDEX IDX_AJS_PRODUCTO ON ALP_AJUSTE_STOCK(PRO_PRODUCTO);
CREATE INDEX IDX_AJS_VARIANTE ON ALP_AJUSTE_STOCK(PVA_PRODUCTO_VARIANTE);
CREATE INDEX IDX_AJS_BODEGA ON ALP_AJUSTE_STOCK(BOD_BODEGA);
CREATE INDEX IDX_AJS_FECHA ON ALP_AJUSTE_STOCK(AJS_FECHA_AJUSTE);

-- ----------------------------------------------------------------------------
-- Tabla: ALP_TOMA_FISICA
-- Descripción: Tomas físicas de inventario (conteos cíclicos)
-- Uso: Conciliación periódica, auditorías
-- ----------------------------------------------------------------------------
CREATE TABLE ALP_TOMA_FISICA (
    TFI_TOMA_FISICA          NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    BOD_BODEGA               NUMBER NOT NULL,
    TFI_FECHA_CONTEO         DATE NOT NULL,
    TFI_FECHA_CIERRE         TIMESTAMP,
    USU_USUARIO              NUMBER NOT NULL,
    TFI_ESTADO               VARCHAR2(20) DEFAULT 'ABIERTO' NOT NULL,
    
    CONSTRAINT FK_TFI_BODEGA FOREIGN KEY (BOD_BODEGA) 
        REFERENCES ALP_BODEGA(BOD_BODEGA),
    CONSTRAINT FK_TFI_USUARIO FOREIGN KEY (USU_USUARIO) 
        REFERENCES ALP_USUARIO(USU_USUARIO),
    CONSTRAINT CHK_TFI_ESTADO CHECK (TFI_ESTADO IN ('ABIERTO', 'CERRADO', 'CONCILIADO', 'CANCELADO'))
);

COMMENT ON TABLE ALP_TOMA_FISICA IS 'Encabezado de tomas físicas de inventario';

CREATE INDEX IDX_TFI_BODEGA ON ALP_TOMA_FISICA(BOD_BODEGA);
CREATE INDEX IDX_TFI_FECHA ON ALP_TOMA_FISICA(TFI_FECHA_CONTEO);
CREATE INDEX IDX_TFI_ESTADO ON ALP_TOMA_FISICA(TFI_ESTADO);

-- ----------------------------------------------------------------------------
-- Tabla: ALP_RESERVA_STOCK
-- Descripción: Reservas temporales de inventario
-- Uso: Carritos de compra, órdenes pendientes de pago
-- ----------------------------------------------------------------------------
CREATE TABLE ALP_RESERVA_STOCK (
    RES_RESERVA_STOCK        NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    PRO_PRODUCTO             NUMBER,
    PVA_PRODUCTO_VARIANTE    NUMBER,
    BOD_BODEGA               NUMBER NOT NULL,
    CLI_CLIENTE              NUMBER,
    RES_CANTIDAD             NUMBER(12,2) NOT NULL,
    RES_FECHA_INICIO         TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    RES_FECHA_EXPIRACION     TIMESTAMP NOT NULL,
    RES_MOTIVO               VARCHAR2(100),
    RES_REFERENCIA_EXTERNA   VARCHAR2(100),
    RES_ESTADO               VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,
    
    CONSTRAINT FK_RES_PRODUCTO FOREIGN KEY (PRO_PRODUCTO) 
        REFERENCES ALP_PRODUCTO(PRO_PRODUCTO),
    CONSTRAINT FK_RES_VARIANTE FOREIGN KEY (PVA_PRODUCTO_VARIANTE) 
        REFERENCES ALP_PRODUCTO_VARIANTE(PVA_PRODUCTO_VARIANTE),
    CONSTRAINT FK_RES_BODEGA FOREIGN KEY (BOD_BODEGA) 
        REFERENCES ALP_BODEGA(BOD_BODEGA),
    CONSTRAINT FK_RES_CLIENTE FOREIGN KEY (CLI_CLIENTE) 
        REFERENCES ALP_CLIENTE(CLI_CLIENTE),
    CONSTRAINT CHK_RES_PRODUCTO_O_VARIANTE CHECK (
        (PRO_PRODUCTO IS NOT NULL AND PVA_PRODUCTO_VARIANTE IS NULL) OR
        (PRO_PRODUCTO IS NULL AND PVA_PRODUCTO_VARIANTE IS NOT NULL)
    ),
    CONSTRAINT CHK_RES_CANTIDAD CHECK (RES_CANTIDAD > 0),
    CONSTRAINT CHK_RES_ESTADO CHECK (RES_ESTADO IN ('ACTIVO', 'LIBERADO', 'CONFIRMADO', 'EXPIRADO'))
);

COMMENT ON TABLE ALP_RESERVA_STOCK IS 'Reservas temporales de inventario (TTL)';
COMMENT ON COLUMN ALP_RESERVA_STOCK.RES_FECHA_EXPIRACION IS 'Fecha de liberación automática';

CREATE INDEX IDX_RES_PRODUCTO ON ALP_RESERVA_STOCK(PRO_PRODUCTO);
CREATE INDEX IDX_RES_VARIANTE ON ALP_RESERVA_STOCK(PVA_PRODUCTO_VARIANTE);
CREATE INDEX IDX_RES_BODEGA ON ALP_RESERVA_STOCK(BOD_BODEGA);
CREATE INDEX IDX_RES_CLIENTE ON ALP_RESERVA_STOCK(CLI_CLIENTE);
CREATE INDEX IDX_RES_ESTADO ON ALP_RESERVA_STOCK(RES_ESTADO);
CREATE INDEX IDX_RES_EXPIRACION ON ALP_RESERVA_STOCK(RES_FECHA_EXPIRACION);

-- ============================================================================
-- BLOQUE 7: PRODUCCIÓN
-- ============================================================================
-- Descripción: Gestión de producción de muebles
-- Incluye: Procesos, recetas, órdenes de producción, costos
-- ============================================================================

-- ----------------------------------------------------------------------------
-- Tabla: ALP_PROCESO
-- Descripción: Procesos de fabricación
-- Ejemplos: 'CORTE', 'ENSAMBLADO', 'LIJADO', 'BARNIZADO', 'TAPIZADO'
-- ----------------------------------------------------------------------------
CREATE TABLE ALP_PROCESO (
    PCO_PROCESO              NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    PCO_CODIGO               VARCHAR2(50) NOT NULL UNIQUE,
    PCO_NOMBRE               VARCHAR2(150) NOT NULL,
    PCO_DESCRIPCION          VARCHAR2(500),
    
    CONSTRAINT CHK_PCO_CODIGO CHECK (LENGTH(PCO_CODIGO) >= 3)
);

COMMENT ON TABLE ALP_PROCESO IS 'Catálogo de procesos de manufactura';

-- ----------------------------------------------------------------------------
-- Tabla: ALP_ESTACION_TRABAJO
-- Descripción: Estaciones o centros de trabajo
-- Ejemplos: 'Área de Corte 1', 'Mesa de Ensamblado 3', 'Cabina de Pintura'
-- ----------------------------------------------------------------------------
CREATE TABLE ALP_ESTACION_TRABAJO (
    EST_ESTACION_TRABAJO     NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    EST_CODIGO               VARCHAR2(50) NOT NULL UNIQUE,
    EST_NOMBRE               VARCHAR2(150) NOT NULL,
    EST_DESCRIPCION          VARCHAR2(500),
    EST_ESTADO               VARCHAR2(20) DEFAULT 'DISPONIBLE' NOT NULL,
    
    CONSTRAINT CHK_EST_ESTADO CHECK (EST_ESTADO IN ('DISPONIBLE', 'OCUPADO', 'MANTENIMIENTO', 'INACTIVO'))
);

COMMENT ON TABLE ALP_ESTACION_TRABAJO IS 'Estaciones de trabajo en planta';

CREATE INDEX IDX_EST_ESTADO ON ALP_ESTACION_TRABAJO(EST_ESTADO);

-- ----------------------------------------------------------------------------
-- Tabla: ALP_RECETA_PRODUCCION
-- Descripción: Recetas maestras de producción (BOM - Bill of Materials)
-- Define qué materiales y en qué cantidades se necesitan
-- ----------------------------------------------------------------------------

CREATE TABLE ALP_RECETA_PRODUCCION (
    RPR_RECETA_PRODUCCION    NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    PRO_PRODUCTO              NUMBER NOT NULL,
    RPR_VERSION               VARCHAR2(20) DEFAULT '1.0' NOT NULL,
    RPR_DESCRIPCION           VARCHAR2(500),
    RPR_ACTIVO                VARCHAR2(1) DEFAULT 'S' NOT NULL,
    RPR_FECHA_CREACION        TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    
    CONSTRAINT FK_RECETA_PRO_PROD FOREIGN KEY (PRO_PRODUCTO) 
        REFERENCES ALP_PRODUCTO(PRO_PRODUCTO),
    CONSTRAINT UNQ_RECETA_PROD_VER UNIQUE (PRO_PRODUCTO, RPR_VERSION),
    CONSTRAINT CHK_RECETA_ACTIVO CHECK (RPR_ACTIVO IN ('S', 'N'))
);

COMMENT ON TABLE ALP_RECETA_PRODUCCION IS 'BOM - Lista de materiales para producción';

CREATE INDEX IDX_RPR_PROD_BUSQUEDA ON ALP_RECETA_PRODUCCION(PRO_PRODUCTO);
CREATE INDEX IDX_RPR_ACTIVO_FILTRO ON ALP_RECETA_PRODUCCION(RPR_ACTIVO);

-- ----------------------------------------------------------------------------
-- Tabla: ALP_RECETA_DETALLE
-- Descripción: Detalle de materiales en la receta
-- Especifica materiales, cantidades y mermas
-- ----------------------------------------------------------------------------
CREATE TABLE ALP_RECETA_DETALLE (
    RDT_RECETA_DETALLE       NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    RPR_RECETA_PRODUCCION    NUMBER NOT NULL,
    MAT_MATERIAL             NUMBER NOT NULL,
    RDT_CANTIDAD             NUMBER(12,2) NOT NULL,
    UNI_UNIDAD_MEDIDA        NUMBER NOT NULL,
    RDT_MERMA_PORCENTAJE     NUMBER(5,2) DEFAULT 0,
    
    CONSTRAINT FK_RDT_RECETA FOREIGN KEY (RPR_RECETA_PRODUCCION) 
        REFERENCES ALP_RECETA_PRODUCCION(RPR_RECETA_PRODUCCION),
    CONSTRAINT FK_RDT_MATERIAL FOREIGN KEY (MAT_MATERIAL) 
        REFERENCES ALP_MATERIAL(MAT_MATERIAL),
    CONSTRAINT FK_RDT_UNIDAD FOREIGN KEY (UNI_UNIDAD_MEDIDA) 
        REFERENCES ALP_UNIDAD_MEDIDA(UNI_UNIDAD_MEDIDA),
    CONSTRAINT CHK_RDT_CANTIDAD CHECK (RDT_CANTIDAD > 0),
    CONSTRAINT CHK_RDT_MERMA CHECK (RDT_MERMA_PORCENTAJE >= 0 AND RDT_MERMA_PORCENTAJE <= 100)
);

COMMENT ON TABLE ALP_RECETA_DETALLE IS 'Materiales y cantidades por receta (BOM detalle)';
COMMENT ON COLUMN ALP_RECETA_DETALLE.RDT_MERMA_PORCENTAJE IS 'Porcentaje de desperdicio esperado';

CREATE INDEX IDX_RDT_RECETA ON ALP_RECETA_DETALLE(RPR_RECETA_PRODUCCION);
CREATE INDEX IDX_RDT_MATERIAL ON ALP_RECETA_DETALLE(MAT_MATERIAL);

-- ----------------------------------------------------------------------------
-- Tabla: ALP_RECETA_PROCESO
-- Descripción: Procesos necesarios en la receta
-- Define el flujo de trabajo (routing)
-- ----------------------------------------------------------------------------
CREATE TABLE ALP_RECETA_PROCESO (
    RPC_RECETA_PROCESO       NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    RPR_RECETA_PRODUCCION    NUMBER NOT NULL,
    PCO_PROCESO              NUMBER NOT NULL,
    RPC_ORDEN                NUMBER NOT NULL,
    RPC_TIEMPO_ESTIMADO_MIN  NUMBER(10,2),
    
    CONSTRAINT FK_RPC_RECETA FOREIGN KEY (RPR_RECETA_PRODUCCION) 
        REFERENCES ALP_RECETA_PRODUCCION(RPR_RECETA_PRODUCCION),
    CONSTRAINT FK_RPC_PROCESO FOREIGN KEY (PCO_PROCESO) 
        REFERENCES ALP_PROCESO(PCO_PROCESO),
    CONSTRAINT UNQ_RPC_RECETA_ORDEN UNIQUE (RPR_RECETA_PRODUCCION, RPC_ORDEN),
    CONSTRAINT CHK_RPC_ORDEN CHECK (RPC_ORDEN > 0),
    CONSTRAINT CHK_RPC_TIEMPO CHECK (RPC_TIEMPO_ESTIMADO_MIN IS NULL OR RPC_TIEMPO_ESTIMADO_MIN > 0)
);

COMMENT ON TABLE ALP_RECETA_PROCESO IS 'Secuencia de procesos en receta (routing)';

CREATE INDEX IDX_RPC_RECETA ON ALP_RECETA_PROCESO(RPR_RECETA_PRODUCCION);
CREATE INDEX IDX_RPC_PROCESO ON ALP_RECETA_PROCESO(PCO_PROCESO);

-- ----------------------------------------------------------------------------
-- Tabla: ALP_ORDEN_PRODUCCION
-- Descripción: Órdenes de producción
-- Autorización para fabricar productos
-- ----------------------------------------------------------------------------
CREATE TABLE ALP_ORDEN_PRODUCCION (
    ORP_ORDEN_PRODUCCION     NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    PRO_PRODUCTO             NUMBER NOT NULL,
    RPR_RECETA_PRODUCCION    NUMBER NOT NULL,
    ORP_NUMERO               VARCHAR2(50) NOT NULL UNIQUE,
    ORP_CANTIDAD             NUMBER(12,2) NOT NULL,
    ORP_FECHA_INICIO         DATE,
    ORP_FECHA_FIN_ESTIMADA   DATE,
    ORP_ESTADO               VARCHAR2(20) DEFAULT 'BORRADOR' NOT NULL,
    
    CONSTRAINT FK_ORP_PRODUCTO FOREIGN KEY (PRO_PRODUCTO) 
        REFERENCES ALP_PRODUCTO(PRO_PRODUCTO),
    CONSTRAINT FK_ORP_RECETA FOREIGN KEY (RPR_RECETA_PRODUCCION) 
        REFERENCES ALP_RECETA_PRODUCCION(RPR_RECETA_PRODUCCION),
    CONSTRAINT CHK_ORP_CANTIDAD CHECK (ORP_CANTIDAD > 0),
    CONSTRAINT CHK_ORP_ESTADO CHECK (ORP_ESTADO IN ('BORRADOR', 'PROGRAMADA', 'EN_PROCESO', 'COMPLETADA', 'CANCELADA')),
    CONSTRAINT CHK_ORP_FECHAS CHECK (ORP_FECHA_FIN_ESTIMADA IS NULL OR ORP_FECHA_FIN_ESTIMADA >= ORP_FECHA_INICIO)
);

COMMENT ON TABLE ALP_ORDEN_PRODUCCION IS 'Órdenes de fabricación';

