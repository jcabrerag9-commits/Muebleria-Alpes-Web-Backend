    SELECT TRL_TRANSACCION_LOG, USU_USUARIO, TRL_ENTIDAD, TRL_FECHA_CREACION 
    FROM ALP_TRANSACCION_LOG 
    ORDER BY TRL_FECHA_CREACION DESC
