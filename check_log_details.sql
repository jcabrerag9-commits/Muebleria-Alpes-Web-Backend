FROM all_tab_columns 
WHERE table_name = 'ALP_TRANSACCION_LOG' 
AND column_name IN ('TRL_TRANSACCION_LOG', 'TRL_FECHA_CREACION');

PROMPT --- Triggers ---
SELECT trigger_name, status 
FROM all_triggers 
WHERE table_name = 'ALP_TRANSACCION_LOG';

